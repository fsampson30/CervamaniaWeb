package model;

public class TipoCerveja {
    
    	private String codigo_tipo_cerveja;
	private String nome_tipo_cerveja;
	private String descricao_tipo_cerveja;

	public String getCodigo_tipo_cerveja() {
		return codigo_tipo_cerveja;
	}

	public void setCodigo_tipo_cerveja(String codigo_tipo_cerveja) {
		this.codigo_tipo_cerveja = codigo_tipo_cerveja;
	}

	public String getNome_tipo_cerveja() {
		return nome_tipo_cerveja;
	}

	public void setNome_tipo_cerveja(String nome_tipo_cerveja) {
		this.nome_tipo_cerveja = nome_tipo_cerveja;
	}

	public String getDescricao_tipo_cerveja() {
		return descricao_tipo_cerveja;
	}

	public void setDescricao_tipo_cerveja(String descricao_tipo_cerveja) {
		this.descricao_tipo_cerveja = descricao_tipo_cerveja;
	}
}
    

